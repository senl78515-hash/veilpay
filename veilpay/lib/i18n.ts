// Recursively widen string literals → string so both locales satisfy the type
type Widen<T> =
  T extends string
    ? string
    : T extends (...args: infer A) => string
    ? (...args: A) => string
    : { [K in keyof T]: Widen<T[K]> }

export type Translations = Widen<typeof translations.en>
